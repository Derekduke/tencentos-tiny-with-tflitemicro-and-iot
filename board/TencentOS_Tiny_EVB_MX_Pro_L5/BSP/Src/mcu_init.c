int fputc(int ch, FILE *f)
{
    if (ch == '\n') {
        HAL_UART_Transmit(&huart2, (void *)"\r", 1, 30000);
    }
    HAL_UART_Transmit(&huart2, (uint8_t *)&ch, 1, 0xFFFF);
    return ch;

}
